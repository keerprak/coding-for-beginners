package com.citi.demo.day2;

public class SwitchCaseDemo {
	
	public static void main(String[] args) {
		
		String input ="A";
		
		switch (input) {
		case "A":
			System.out.println("Attractive");
			break;
		case "B":
			System.out.println("Beautiful");
			break;
		case "C":
			System.out.println("Charm");
			break;
		default:
			System.out.println("No comments");
			break;
		
		}
		
			
	}

}
